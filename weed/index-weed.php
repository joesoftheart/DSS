&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;การปลูกข้าวแบบนาหว่านมีการปฏิบัติที่หลากหลาย เช่น การหว่านสำรวย การหว่านคราดกลบหรือไถกลบ และการหว่านหลังขี้ไถ<img height="200" width="300" align="right" border="0" alt="" src="../rice/rice206weed_011s.gif" tppabs="http://localhost/rice/vutchapurd/../rice/rice206weed_011s.gif" /> ทั้ง 3 รูปแบบเป็นการปลูกข้าวที่มีข้อแตกต่างตรงวิธีการเตรียมดิน และการปลูก โดยการเตรียมดินมีวัตถุประสงค์เพื่อกำจัดวัชพืชดั้งเดิมที่ขึ้นอยู่ในนา ก่อนที่จะหว่านข้าว ซึ่งวิธีการเตรียมดินของเกษตรกรในปัจจุบัน ใช้เครื่องจักรแทนแรงงานสัตว์เป็นส่วนใหญ่ เครื่องมือที่ใช้ เช่น รถไถเดินตาม หรือรถแทรกเตอร์ขนาดใหญ่ และการเตรียมดินจะพิถีพิถันมากหรือน้อยเพียงใด ขึ้นอยู่กับเครื่องมือ ค่าใช้จ่าย และความพร้อมของเกษตรกรเอง โดยมีวิธีการปลูกร่วมกับการใช้อัตราเมล็ดพันธุ์ในการทำนาหว่านข้าวแห้งของภาคตะวันออกเฉียงเหนือ พบว่าวิธีการเตรียมดินและปลูกตามแบบต่างๆ ทั้ง 3 แบบ ซึ่งเป็นวิธีของเกษตรกรและใช้เมล็ดพันธุ์ข้าวอัตราตั้งแต่ต่ำสุดที่ 8 กิโลกรัม ถึงสูงสุดที่ 24 กิโลกรัมต่อไร่ ทำให้ผลผลิตข้าวขาวดอกมะลิ 105 ที่ได้รับไม่แตกต่างกันทุกวิธีการปลูก และทุกอัตราเมล็ดพันธุ์ โดยเฉลี่ยประมาณ 265 กิโลกรัมต่อไร่
&nbsp;
<a name="4"></a><strong>อัตราเมล็ดพันธุ์</strong><br />
<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ตามปกติในการทำนาหว่านข้าวแห้ง มีคำแนะนำให้ใช้เมล็ดพันธุ์ประมาณ 1-2 ถังต่อไร่ หรือประมาณ 10-20<img height="200" width="300" align="right" border="0" alt="" src="../rice/rice206weed_008s.gif" tppabs="http://localhost/rice/vutchapurd/../rice/rice206weed_008s.gif" /> กิโลกรัมต่อไร่ การใช้เมล็ดพันธุ์อัตราดังกล่าว จะทำให้มีประชากรของต้นข้าวที่เหมาะสม สำหรับในเรื่องอัตราเมล็ดพันธุ์ข้าวเกษตรกรจะให้ความสนใจมาก บางพื้นที่ใช้อัตราเมล็ดพันธุ์ 40-50 กิโลกรัมต่อไร่ เพื่อชดเชยการสูญเสียจากการทำลายของสัตว์ศัตรูข้าว เช่น นก หนู เป็นต้น และเพิ่มการ แข่งขันระหว่างข้าวกับวัชพืชในแปลงนา การใช้อัตราที่เหมาะสมประมาณ 16 กิโลกรัมต่อไร่ จะทำให้ประชากรต้นข้าวที่เกิดขึ้นมาสามารถที่จะแข่งขันกับวัชพืชได้ มีการวิจัยและนำเอาวิธีการดังกล่าวมาผสมผสานร่วมกัน โดยที่เมื่อทำการเพิ่มอัตราเมล็ดพันธุ์ให้สูงขึ้นจะมีผลทำให้ชนิด และจำนวนวัชพืชที่เกิดขึ้นในแปลงปลูกข้าวลดลง ตามอัตราการเพิ่มขึ้นของเมล็ดพันธุ์ การทำนาหว่านข้าวแห้งด้วยการใช้เมล็ดพันธุ์อัตรา 18-24 กิโลกรัมต่อไร่ จะช่วยลดปัญหาวัชพืชให้ลดลง อย่างไรก็ตามวิธีการดังกล่าวจะเกิดประโยชน์ในการแนะนำ ให้เกษตรกรปลูกข้าวแบบการทำนาหว่านข้าวแห้ง ตามสภาพความเหมาะสมของพื้นที่ และความพร้อมของเกษตรกร โดยการจัดการวิธีการเตรียมดินและวิธีการปลูกแต่ใช้เมล็ดพันธุ์ที่เหมาะสม โดยลดต้นทุนการผลิตข้าวในด้านแรงงานและค่าใช้จ่ายต่างๆ เพื่อลดปัญหาวัชพืชในนาหว่านข้าวแห้งได้ในระดับหนึ่ง โดยลดต้นทุนการผลิตข้าวที่จะต้องใช้แรงงานเตรียมดิน และอัตราเมล็ดพันธุ์ได้อย่างเหมาะสม
&nbsp;
<a name="5"></a><strong>การกำหนดช่วงเวลาปลูก<br />
</strong><br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;พันธุ์ข้าวขาวดอกมะลิ 105, กข15 และกข6 เป็นพันธุ์ข้าวไวต่อช่วงแสง เหมาะสำหรับการปลูกในฤดูนาปี ในภาคตะวันออกเฉียงเหนือ เกษตรกรจะเริ่มไถเตรียมดินและหว่านข้าวตั้งแต่ปลายเดือนเมษายนเป็นต้นไป การที่เกษตรกรหว่านข้าวเร็วจะทำให้ต้นข้าวอยู่ในนาเป็นเวลานาน และจะประสบกับปัญหาจากสภาพความแห้งแล้งเนื่องจากฝนทิ้งช่วง มีวัชพืชขึ้นแข่งขันมาก และแมลงสัตว์ศัตรูข้าวรบกวน การกำหนดช่วงระยะปลูกที่เหมาะสมจะช่วยลดปัญหาจากวัชพืชในนาหว่านข้าวแห้งได้ มีคำแนะนำให้หว่านข้าวประมาณเดือนมิถุนายนจนถึงกลางเดือนสิงหาคมแทนที่จะปลูกในเดือนเมษายน จากการทดสอบศึกษาช่วงเวลาปลูกข้าวพันธุ์ขาวดอกมะลิ 105 และพันธุ์กข6 โดยเริ่มปลูกตั้งแต่ปลาย เดือนพฤษภาคม จนถึงเดือนกรกฎาคม พบว่า การกำหนดระยะเวลาปลูกเร็วหรือช้าจะมีผลกระทบต่อการให้ผลผลิตของข้าวทั้ง 2 พันธุ์ โดยที่การปลูกประมาณกลางเดือนกรกฎาคมจะให้ผลผลิตสูงสุด และมีปัญหาวัชพืชต่ำ การปลูกข้าวทั้ง 2 พันธุ์นี้เร็วแต่ต้นปี จะทำให้ข้าวประสบกับสภาวะแห้งแล้งหรือฝนทิ้งช่วง และเกิดปัญหาจากวัชพืชรุนแรงติดตามมา
&nbsp;
<a name="6"></a><strong>วิธีการ การควบคุมวัชพืชร่วมกับอัตราเมล็ดพันธุ์</strong><br />
<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;พันธุ์ข้าวที่แนะนำ และส่งเสริมให้ปลูกในนาหว่านข้าวแห้งในภาคตะวันออกเฉียงเหนือ ที่เป็นพันธุ์พื้นเมืองเช่นพันธุ์ขาวดอกมะลิ 105, เหลืองประทิว 123, ขาวตาแห้ง 17, กข15 และ กข6 เป็นต้น ส่วนใหญ่เป็นพันธุ์ข้าวที่มีลักษณะทรงต้นสูง ใบยาวใหญ่แผ่ โน้มปกคลุม เป็นลักษณะที่มีความสามารถในการแข่งขันกับวัชพืชได้ดี การใช้พันธุ์พืชปลูกที่สามารถแข่งขันกับวัชพืชได้ดี จัดเป็นวิธีการหนึ่งของการจัดการวัชพืช มีการศึกษาพันธุ์ข้าว 2 พันธุ์ที่มีลักษณะรูปทรงต้นแตกต่างกัน คือ พันธุ์กข23 และพันธุ์ขาวดอกมะลิ 105 ซึ่งมีความสามารถในการแข่งขันกับวัชพืชต่างกัน พบว่าใช้พันธุ์ข้าว กข23 การใช้อัตราเมล็ดพันธุ์ 32 กิโลกรัมต่อไร่ ให้ผลผลิตสูงสุด ส่วนพันธุ์ขาวดอกมะลิ 105 เมล็ดพันธุ์ที่อัตรา 16 กิโลกรัมต่อไร่ จะให้ผลผลิตสูงสุด การเพิ่มอัตราเมล็ดพันธุ์ตั้งแต่ 8 ถึง 40 กิโลกรัมต่อไร่ ในสภาพที่ไม่มีการกำจัดวัชพืช จะทำให้น้ำหนักแห้งของวัชพืชลดลงตามอัตราการเพิ่มขึ้นของเมล็ดพันธุ์ เนื่องจากอัตราเมล็ดพันธุ์ดังกล่าวเป็นอัตราที่ทำให้มีประชากรต้นข้าว และความหนาแน่นของต้นข้าวแข่งขันกับวัชพืชได้อย่างเหมาะสม ในทำนองเดียวกันการกำจัดวัชพืชโดยการถอนด้วยมือ 2 ครั้ง ที่ระยะ15 และ30 วันหลังข้าวงอก จะทำให้ผลผลิตข้าวเพิ่มสูงขึ้นประมาณ 84 เปอร์เซ็นต์ เมื่อเปรียบเทียบกับไม่มีการควบคุมวัชพืช

