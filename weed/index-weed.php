<a name="1"></a><strong>การจำแนกชนิดวัชพืช</strong><br />
<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;การเปลี่ยนวิธีการทำนาจากนาดำเป็นนาหว่านข้าวแห้ง ทำให้ระบบนิเวศการปลูกข้าวเปลี่ยนแปลงไป เริ่มตั้งแต่ช่วงระยะการปลูก การเปลี่ยนพันธุ์ข้าว การเตรียมดินและการจัดการอื่นๆ ทำให้ชนิด และปริมาณวัชพืชมีการเปลี่ยนแปลงไปด้วย จากการสำรวจประชากรวัชพืช ในสภาพการทำนาหว่านข้าวแห้ง พบว่าในระยะแรกๆ ของการเจริญเติบโตทางลำต้นหรือประมาณ 30 วันของข้าวหลังจากข้าวงอก ชนิดวัชพืชเด่นที่พบ เช่น หญ้านกสีชมพู (Echinochloa colon (L.) Link), เซ่งใบมน (Melochia corchorifolia L.) และกกต่างๆ เช่น กกทราย (Cyperus iria L.), เป็นต้น แต่ในระยะ 60 วันหลังข้าวงอก หรือต้นข้าวมีการเจริญเติบโตในช่วงเริ่มแตกกอ จะพบหญ้านกสีชมพู ในระยะเก็บเกี่ยวจะพบหญ้าแดง (Ischaemum rugosum Salisb.) และหญ้าไทร (Leersia hexandra SW.) มีจำนวนมากกว่าชนิดอื่นๆ การเปลี่ยนแปลงชนิดและจำนวนของวัชพืช ตั้งแต่ต้นฤดูปลูกจนถึงเก็บเกี่ยว แตกต่างกันไป โดยที่ในระยะแรกของการปลูก จะพบวัชพืชพวกวงศ์หญ้า และวัชพืชใบกว้างเกิดขึ้นมากกว่าประเภทอื่นๆ แต่ในระยะเก็บเกี่ยวจะพบว่าปริมาณวัชพืชพวกกกมีจำนวนมากขึ้น ทั้งนี้คาดว่าเกิดจากการเปลี่ยนแปลงของความชื้น และระดับน้ำในนา ตลอดจนการเจริญเติบโตของต้นข้าวทั้งความสูงและพื้นที่ใบ ซึ่งจะมีผลต่อการเปลี่ยนแปลงชนิดและปริมาณวัชพืชด้วยเช่นกัน

