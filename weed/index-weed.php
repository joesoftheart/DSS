&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;การเตรียมแปลงปลูกที่ดี จะช่วยลดปัญหาจากวัชพืชได้ แต่ในสภาพการทำนาหว่านข้าวแห้ง ในพื้นที่อาศัยน้ำฝน เมื่อปริมาณและการกระจายตัวของฝนเหมาะสม ทำให้เมล็ดข้าวที่อยู่ในดินงอกขึ้นมาขณะเดียวกันเมล็ดวัชพืชที่อยู่ในระดับผิวดินก็จะงอกขึ้นมาพร้อมกับต้นข้าวและการแข่งขันในปัจจัยการเจริญเติบโตกับต้นข้าวจึงเริ่มขึ้น อีกกรณีหนึ่งการไถเป็นการกำจัดวัชพืชที่เกิดขึ้นมา หรือทำลายเมล็ดวัชพืชในระดับที่จะงอกได้ ในเวลาเดียวกันจะเป็นการพลิกให้เมล็ดวัชพืชที่อยู่ลึกในดินขึ้นมาสู่ผิวดิน และสามารถงอกขึ้นมาอีก

