<a name="2"></a><strong>พันธุ์ข้าว </strong><br />
<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;พันธุ์ข้าวที่มีรูปทรงต้นและลักษณะบางประการ มีความสามารถในการแข่งขันกับวัชพืชได้ค่อนข้างสูง เช่น ลักษณะทรงต้นสูง ใบแผ่กว้างจะช่วยปกคลุมพื้นที่ปิดกั้นบดบังแสงมิให้แสงส่องผ่านลงสู่ต้นล่าง ทำให้ปริมาณและความเข้มของแสงที่ผิวดินต่ำลง ทำให้การงอกของเมล็ดวัชพืชบางชนิดลดลง และชะลอการเจริญเติบโตของต้นกล้าวัชพืชลงได้ ในสภาพนาหว่านข้าวแห้งได้มีการศึกษาวิจัยพันธุ์ข้าวที่มีความสามารถในการแข่งขันกับวัชพืช ซึ่งมีวัตถุประสงค์เพื่อต้องการศึกษาความสามารถในการแข่งขันกับวัชพืชของข้าวสายพันธุ์ดีเด่นบางพันธุ์ในนาหว่านข้าวแห้งในภาคตะวันออกเฉียงเหนือ ผลการทดลองพบว่าในสภาพที่ปล่อยให้มีการแข่งขันกับวัชพืช ในข้าวพันธุ์ขาวดอกมะลิ 105, และกข23 จะพบจำนวนต้น และ น้ำหนักแห้งวัชพืชเหลืออยู่ค่อนข้างต่ำ <br />
จากผลการทดลองดังกล่าวนี้ แสดงว่าพันธุ์ข้าวขาวดอกมะลิ 105 ที่มีลักษณะทรงต้นสูง ใบยาวและแผ่กว้างทำให้วัชพืชไม่สามารถเจริญเติบโตได้เต็มที่ หรือลดจำนวนลง โดยเฉพาะอย่างยิ่งพันธุ์ขาวดอกมะลิ 105 จะให้ผลผลิตสูงกว่าพันธุ์อื่นๆ เมื่อมีการแข่งขันกับวัชพืชในสภาพการทำนาน้ำฝน

