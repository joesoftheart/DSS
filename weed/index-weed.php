&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;เป็นเวลามากกว่า 10 ปีที่ผ่านมาเกษตรกรในภาคตะวันออกเฉียงเหนือ ปรับเปลี่ยนวิธีการปลูกข้าว จากการทำนาดำ เป็นนาหว่านแห้งกันมากขึ้น จากรายงานของสำนักงานเศรษฐกิจการเกษตร(2541) พบว่าพื้นที่การทำนาหว่านข้าวแห้ง ปี 2540/41 ประมาณ 8.17 ล้านไร่ การที่เกษตรกรหันมาทำนาหว่านข้าวแห้งเพิ่มมากขึ้น เนื่องจากเป็นวิธีการปลูกข้าวที่ประหยัดแรงงาน เวลา และค่าใช้จ่าย เพราะการปลูกข้าวแบบนาหว่านข้าวแห้ง เป็นการปลูกข้าวแบบหว่านเมล็ดโดยตรง เมล็ดข้าวจะงอกพร้อมกัน เมื่อได้รับน้ำฝนหรือมีความชื้นสภาพแวดล้อมเหมาะสม แต่ปัญหาสำคัญที่ตามมา คือ ปัญหาจากวัชพืชในนาข้าว หากไม่มีการควบคุมและกำจัดวัชพืชแล้ว จะเกิดการสูญเสีย และมีผลทำให้ผลผลิตของข้าวในการทำนาหว่านข้าวแห้งลดลง ประมาณ 30 เปอร์เซ็นต์<br />
<br />
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;การควบคุมและกำจัดวัชพืชในนาหว่านข้าวแห้ง ที่เกษตรกรปฏิบัติอยู่ส่วนใหญ่เป็นวิธีดั้งเดิม เช่น การเตรียมดินปลูกที่ดีและการใช้แรงงานถอนกำจัดวัชพืช แต่การถอนวัชพืชด้วยมือในนาหว่านข้าวแห้ง จะกระทำได้ยาก สิ้นเปลืองแรงงาน มีค่าใช้จ่ายสูง และทำลายต้นข้าวขณะปฏิบัติงาน นอกจากนี้ประสิทธิภาพในการกำจัดวัชพืชค่อนข้างต่ำอีกด้วย การจัดการวัชพืชแบบผสมผสาน (Integrated Weed Management) เป็นการควบคุมและกำจัดวัชพืชที่เป็นระบบอย่างต่อเนื่อง มีการนำวิธีการหลายๆ วิธีมาใช้ร่วมกัน องค์ประกอบของการจัดการวัชพืช เช่น การจำแนกชนิดวัชพืช วิธีการเขตกรรม การใช้อัตราปลูกที่เหมาะสม การคัดเลือกพันธุ์ที่เหมาะสม การปลูกพืชร่วมระบบและการปลูกพืชหมุนเวียน การใช้ประโยชน์จากมวลชีวภาพมาใช้ร่วมกัน หรืออาจจะประสานกับการปฏิบัติด้านอื่นๆ ด้วย เพื่อให้ได้ผลที่สมบูรณ์โดยประหยัดค่าใช้จ่าย ทั้งนี้จะต้องคำนึงถึงผลกระทบต่อสภาพแวดล้อมที่จะตามมาอีกด้วย

