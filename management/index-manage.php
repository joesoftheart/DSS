                                <strong><a name="a08"></a>การตกกล้าในสภาพดินแห้ง</strong> การตกกล้าโดยวิธีนี้ ควรกระทำเมื่อฝนไม่ตกตามปกติ และไม่มีน้ำเพียงพอที่จะทำเทือกเพื่อตกกล้าได้ แต่มีน้ำพอที่จะใช้รดแปลงกล้าได้ มีวิธีการปฏิบัติดังนี้<br />
                                  <br />
                                  <strong> </strong>- การเตรียมดิน เลือกแปลงที่ดอนน้ำไม่ท่วม มีการระบายน้ำดี อยู่ใกล้แหล่งน้ำที่จะนำมารดแปลง ทำการไถดะตากดินให้แห้ง แล้วไถแปร คราดดินให้แตกละเอียด เก็บวัชพืชออก ปรับระดับดินให้ราบเรียบ <br />
                                  <br />
                                  <strong> </strong>- การตกกล้า ทำได้ 4 แบบคือ
                                
                                  
                                    1. การหว่านข้าวแห้ง หว่านเมล็ดพันธุ์ลงในแปลงโดยตรง โดยไม่ต้องเพาะเมล็ดให้งอกก่อน ใช้อัตราเมล็ดพันธุ์เช่นเดียวกับการตกกล้าเทือก คือประมาณ 80-90 กิโลกรัมต่อไร่ แล้วคราดกลบเมล็ดพันธุ์ให้จมดินพอประมาณ อย่าให้จมมาก เพราะจะทำให้เมล็ดงอกช้าและโคนกล้าอยู่ลึกทำให้ถอนยาก<br />
                                      <br />
                                      2. การหว่านข้าวงอก เพาะเมล็ดให้งอกขนาดตุ่มตา (วิธีการเพาะเช่นเดียวกับการตกกล้าเทือก) อัตราเมล็ดพันธุ์เช่นเดียวกับการหว่านข้าวแห้ง ควรหว่านตอนบ่ายหรือเย็น หว่านแล้วคราดกลบและรดน้ำให้ชุ่มทันทีหลังการหว่าน<br />
                                      <br />
                                      3. การตกกล้าแบบกระทุ้งหยอดข้าวแห้ง หรือวิธีการซิมกล้า เป็นวิธีการที่เหมาะสมกับสภาพนาดอนอาศัยน้ำฝน โดยการไถพรวนดินให้ดินร่วน เพื่อกำจัดวัชพืชและสะดวกต่อการงอกของเมล็ด จากนั้นใช้ไม้กระทุ้งหยอดเมล็ดลงหลุม แล้วใช้ดินหรือขี้เถ้าแกลบกลบเมล็ดเพื่อป้องกันสัตว์เลี้ยงหรือแมลง มาคุ้ยเขี่ย หลังจากนั้นจึงถอนกล้าจากแปลงกล้านี้ไปปักดำในแปลงปักดำ ซึ่งคิดเป็นอัตราเมล็ดพันธุ์ที่ใช้ในการปักดำต่อพื้นที่ 1 ไร่ ต้องใช้เมล็ดพันธุ์ประมาณ 12-15 กิโลกรัมต่อไร่
                                    4. การตกกล้าสำหรับใช้กับเครื่องปักดำ
                                  
                                
