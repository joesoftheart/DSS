                                  <br />
                                  <strong> </strong>- การตกกล้า การตกกล้ามีหลายวิธีการ ขึ้นอยู่กับสภาพแวดล้อมและวัตถุประสงค์ เช่นการตกกล้าบนดินเปียก (ทำเทือก) การตกกล้าบนดินแห้ง และการตกกล้าใช้กับเครื่องปักดำข้าว
                                <strong> <a name="a07"></a>การตกกล้าในสภาพเปียก</strong> หรือ<strong>การตกกล้าเทือก</strong> เป็นวิธีที่ชาวนาคุ้นเคยกันดี การตกกล้าแบบนี้จะต้องมีน้ำหล่อเลี้ยงอยู่เสมอ การดูแลรักษาไม่ยุ่งยากและความสูญเสียจากการทำลายของศัตรูข้าวมีน้อย มีขั้นตอนการปฏิบัติดังนี้<br />
                                  <br />
                                  <strong> </strong>- การเตรียมดิน ปฏิบัติเช่นเดียวกับแปลงปักดำ แต่เพิ่มความพิถีพิถันมากขั้น ในการเก็บกำจัดวัชพืช และปรับระดับเทือกให้ราบเรียบสม่ำเสมอ <br />
                                  <br />
                                  <strong> </strong>- การเพาะเมล็ดพันธุ์ ปฏิบัติตามขั้นตอนของการเตรียมเมล็ดพันธุ์ การแช่และหุ้มเมล็ดพันธุ์ โดยใช้อัตราเมล็ดพันธุ์ 50-60 กรัมต่อตารางเมตร หรือประมาณ 80-90 กิโลกรัมต่อไร่ จะได้กล้าสำหรับปักดำได้ประมาณ 15-20 ไร่ <br />
                                  <br />
                                  <strong> </strong>- การหว่านเมล็ดพันธุ์ ปล่อยน้ำแปลงกล้าให้แห้ง ทำเทือกให้ราบเรียบสม่ำเสมอ นำเมล็ดพันธุ์ที่เพาะงอกดีแล้วมาหว่านให้กระจายสม่ำเสมอตลอดแปลง ควรหว่านเมล็ดพันธุ์ตอนบ่ายหรือตอนเย็น เพื่อหลีกเลี่ยงแสงแดดตอนเที่ยงซึ่งมีความร้อนแรงมาก อาจทำให้เมล็ดข้าวตายได้<br />
                                  <br />
                                  <strong> </strong>- การให้น้ำ ถ้าตกกล้าไม่มากนัก หลังจากหว่านเมล็ดพันธุ์แล้วหนึ่งวัน สาดน้ำรดให้กระจายทั่วแปลง ประมาณ 3-5 วัน กล้าจะสูงพอที่ไขน้ำเข้าท่วมแปลงได้ แต่ถ้าตกกล้ามาก ไม่สามารถที่จะสาดน้ำรดได้ ให้ปล่อยน้ำหล่อเลี้ยงระหว่างแปลงย่อย ประมาณ 3-5 วัน เมื่อต้นกล้าสูงจึงไขน้ำเข้าท่วมแปลง และค่อยเพิ่มระดับขึ้นเรื่อยๆ ตามความสูงของต้นกล้าจนน้ำท่วมผิวดินตลอด ให้หล่อเลี้ยงไว้ในระดับลึกประมาณ 5-10 เซนติเมตร จนกว่าจะถอนกล้าไปปักดำ<br />
                                  <br />
                                  <strong> </strong>- การใส่ปุ๋ยเคมี ถ้าดินแปลงกล้ามีความอุดมสมบูรณ์สูง กล้างามดีก็ไม่จำเป็นต้องใส่ปุ๋ย เพราะจะงามเกินไป ใบจะยาว ต้นอ่อน ทำให้ถอนแล้วต้นขาดง่ายและตั้งตัวได้ช้าเมื่อนำไปปักดำ แต่ถ้าดินมีความอุดมสมบูรณ์ต่ำ ให้ใส่ปุ๋ยเคมีแอมโมเนียมฟอสเฟต (16-20-0) อัตราประมาณ 25-40 กิโลกรัมต่อไร่ โดยใส่หลังหว่านเมล็ดพันธุ์แล้วประมาณ 7 วัน หรือเมื่อสามารถไขน้ำเข้าท่วมแปลงได้แล้ว (ดูรายละเอียดในเรื่องการใส่ปุ๋ยแปลงกล้า)<br />
