                                <strong><a name="a05"></a>ข้อควรระวังในการเตรียมดิน<br />
                                  </strong><br />
                                  1. ควรปล่อยให้ดินนามีโอกาสแห้งสนิท เป็นระยะเวลานานพอสมควร และถ้าสามารถไถพลิกดินล่างขึ้นมาตากให้แห้งได้ก็จะดียิ่งขึ้น ถ้าดินเปียกน้ำติดต่อกันโดยไม่มีโอกาสแห้ง จะเกิดการสะสมของสารพิษ เช่นแก๊สไข่เน่า (ไฮโดรเจนซัลไฟด์) และกรดอินทรีย์ เป็นต้น ซึ่งถ้าสารเหล่านี้มีปริมาณมากก็จะเป็นอันตรายต่อรากข้าวได้ <br />
                                  <br />
                                  2. ควรมีการหมักฟาง หญ้ารวมทั้งอินทรียวัตถุเพื่อให้สลายตัวสมบูรณ์ ประมาณ 2 สัปดาห์ หลังการไถเตรียมดิน เพื่อให้ ดินปรับตัวอยู่ในสภาพที่เหมาะสมต่อการเจริญเติบโตของข้าว และสามารถปลดปล่อยธาตุอาหารที่จำเป็นออกมาให้แก่ต้นข้าว<br />
                                  <br />
                                  3. ดินกรดจัดหรือดินเปรี้ยวจัด มีค่าความเป็นกรดเป็นด่างต่ำ (pH ต่ำกว่า4.0) ควรขังน้ำไว้อย่างน้อย 1 เดือน ก่อนปักดำข้าว เพื่อให้ปฏิกิริยาต่างๆ ตลอดจนความเป็นกรดของดินลดลงสู่สภาวะปกติ และค่อนข้างเป็นกลางเสียก่อน ดินกลุ่มนี้ถ้ามีการขังน้ำตลอดปี หรือมีการทำนาปีละ 2 ครั้ง ก็จะเป็นการลดสภาวะความเป็นกรดของดิน และการเกิดสารพิษลงได้ ซึ่งจะทำให้ผลผลิตของข้าวสูงขึ้น
                                <strong><a name="a06"></a>การตกกล้า</strong><br />
                                  <br />
                                  การเตรียมต้นกล้าให้ได้ต้นที่แข็งแรง เมื่อนำไปปักดำก็จะได้ข้าวที่เจริญเติบโตได้รวดเร็ว และมีโอกาสให้ผลผลิตสูง ต้นกล้าที่แข็งแรงดีต้องมีการเจริญเติบโตและความสูงสม่ำเสมอทั้งแปลง มีกาบใบสั้น มีรากมากและรากขนาดใหญ่ ไม่มีโรคและแมลงทำลาย <br />
                                  <br />
                                  <strong> </strong>- การเตรียมเมล็ดพันธุ์ ต้องเป็นเมล็ดพันธุ์ที่บริสุทธ์ ปราศจากสิ่งเจือปน มีเปอร์เซ็นต์ความงอกสูง (ไม่ต่ำกว่า 80 เปอร์เซ็นต์) ปราศจากการทำลายของโรคและแมลง<br />
                                  <br />
                                  <strong> </strong>- การแช่และหุ้มเมล็ดพันธุ์ นำเมล็ดข้าวที่ได้เตรียมไว้บรรจุในภาชนะเช่นตะกร้าไม้ไผ่สาน กระสอบป่านหรือ ถุงผ้า ไปแช่ในน้ำสะอาด นานประมาณ 12-24 ชั่วโมง จากนั้นนำเมล็ดพันธุ์ขึ้นมาวางบนพื้นที่น้ำไม่ขัง และมีการถ่ายเทอากาศดี นำกระสอบป่านชุบน้ำจนชุ่มมาหุ้มเมล็ดพันธุ์โดยรอบ รดน้ำทุกเช้าและเย็น เพื่อรักษาความชุ่มชื้น หุ้มเมล็ดพันธุ์ไว้นานประมาณ 30-48 ชั่วโมง เมล็ดข้าวจะงอกขนาด “ตุ่มตา” (มียอดและรากเล็กน้อยโดยรากจะยาวกว่ายอด) พร้อมที่จะนำไปหว่านได้
                              
                              
                              
