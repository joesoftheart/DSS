<strong><u><a name="a2"></a>แมลงศัตรูข้าวไร่และการป้องกันกำจัด</u></strong>
        แมลงศัตรูข้าวไร่มีหลายชนิด นอกจากจะเป็นแมลงศัตรูข้าวชนิดที่ระบาดทำลายในนาข้าว ทั่วไป ซึ่งมีปริมาณน้อยและจัดว่าไม่ค่อยมีความสำคัญในการเพาะปลูกข้าวไร่แล้ว ยังมีชนิดที่สำคัญและระบาดทำความเสียหายอยู่เสมอ โดยเฉพาะในการเพาะปลูกข้าวไร่ในภาคเหนือ ได้แก่ มดง่าม ปลวก แมลงวันเจาะยอด ด้วงหมัดดำ ด้วงแทะใบ แมลงค่อมทอง แมลงนูน ด้วงดีด เพลี้ยอ่อน หนอนใย ตั๊กแตน เพลี้ยแป้ง
1. <strong>มดง่าม</strong> (Ant : <em>Pheidole</em> sp.) วงศ์ Formicidae อันดับ Hymenoptera<br />
        เป็นแมลงศัตรูที่ทำให้เกิดปัญหาในระยะหยอดเมล็ด เมื่อเกษตรกรหว่านโรยหรือหยอดเมล็ดพันธุ์ข้าวไร่ลงไปในดิน มดง่ามซึ่งอาศัยอยู่ในรังในดิน จะขนเมล็ดข้าวนำไปเป็นอาหารหรือเก็บสะสมเป็นอาหารในรัง ทำให้สูญเสียเมล็ดพันธุ์ที่ใช้เพาะปลูก ก่อปัญหาในด้านจำนวนกอต่อพื้นที่และการซ่อมเมล็ด ทำให้ผลผลิตลดลง
<em>การป้องกันกำจัด</em><br />
         1. ถ้าพบรังหรือทางเดินของมดง่าม ใช้สารฆ่าแมลงคาร์บาริล (เซฟวิน 85 %) หรือเฮบตาคลอ (อาลามอน 40 %) ชนิดผงโรยที่รังหรือทางเดินของมดง่าม<br />
        2. ถ้าพบรังมดง่ามอยู่กระจัดกระจายทั่วแปลงเพาะปลูก หรือบริเวณใกล้เคียงใช้วิธีคลุกเมล็ดพันธุ์ข้าวไร่ด้วยสารฆ่าแมลงชนิดผง เช่น คาร์บาริล (เซฟวิน 85 %) เมทธิโอคาร์บ (เมซูโรล 50 %) เฟนวาลีเลต (ซูมิไซดิน 20 %) เตทตระคลอร์วินฟอส (การ์โดนา 50 %) ในอัตรา 1 % ต่อน้ำหนักเมล็ด (สารฆ่าแมลง 85 % ใช้อัตรา 12 กรัมต่อเมล็ดข้าว 1 กิโลกรัม สารฆ่าแมลง 50 % ใช้อัตรา 20 กรัมต่อเมล็ดข้าว 1 กิโลกรัม) ควรทำการคลุกให้สารฆ่าแมลงติดเมล็ดสม่ำเสมอที่สุดแล้วนำไปปลูกทันที
2. <strong>ปลวก</strong> <br />
        กัดกินทำลายส่วนรากของข้าวไร่ทุกระยะ และยังกัดกินทำลายโดยเริ่มจากส่วนใต้ดินขึ้นไปตามภายในลำต้น ต้นข้าวที่ถูกทำลายในระยะแรกจะมีลำต้นเหลืองหรือเหลืองซีดทั้งกอและแห้งตายในเวลาต่อมา
<em>การป้องกันกำจัด</em><br />
         1. ขณะทำการเตรียมดิน ถ้าพบรังปลวกใต้ดินให้ขุดทำลายรังหรือไถพรวนดินหลายๆ ครั้ง เพื่อเป็นการทำลายรังและเปิดโอกาสให้มดและนกชนิดต่างๆ เข้าช่วยกินปลวก<br />
        2. ถ้าพบรังปลวกมากและอยู่กระจายในแปลงเพาะปลูก ควรพ่นด้วยสารฆ่าแมลงชนิดผงละลายน้ำ เช่น เฮบตาคลอ (อาลามอน 40 %) อัตรา 2 กิโลกรัมต่อไร่ พ่นให้ทั่วแปลงแล้วพรวนให้เข้ากับดินหรืออาจใช้วิธีพ่นหรือโรยตามแถวปลูก
3. <strong>เพลี้ยอ่อน</strong> (Rice root aphid : <em>Tetraneura nigriabdominalis</em> Sasaki) วงศ์ Aphididae อันดับ Homoptera<br />
(วิเคราะห์ตัวอย่างแมลง โดย อาจารย์วาลุลี โรจนวงศ์ ภาควิชากีฏวิทยา คณะเกษตรศาสตร์ มหาวิทยาลัยเกษตรศาสตร์)<br />
        เป็นแมลงขนาดเล็กเคลื่อนไหวช้ามีขนาด 1 – 2 มม. สีน้ำตาล หรือน้ำตาลแดงรูปร่างคล้ายผลฝรั่งผ่าครึ่ง และเป็นเพลี้ยอ่อนชนิดไม่มีปีก การทำลายพบอาศัยเกาะดูดกินน้ำเลี้ยงอยู่ที่ส่วนรากในดินของต้นข้าว ในระยะเริ่มแตกกอและเกาะเป็นกลุ่มๆ บางครั้งพบเกาะดูดกินส่วนรากของต้นข้าวที่อยู่โคนต้นใกล้ระดับดิน ต้นข้าวมีอาการเหลืองซีดและเตี้ยแคระแกรนผิดปกติ ถ้ามีการระบาดรุนแรงต้นข้าวจะแสดงอาการเหี่ยวเฉาและตายในเวลาต่อมา<br />
        นอกจากนี้เพลี้ยอ่อนชนิดอาศัยดูดกินน้ำเลี้ยงบริเวณรากแล้ว ยังพบเพลี้ยอ่อนอีกชนิดหนึ่ง คือ Rhopalosiphum rufiabdominalis (วิเคราะห์ตัวอย่างแมลง โดย Dr.Hans Banziger ภาควิชากีฏวิทยา คณะเกษตรศาสตร์ มหาวิทยาลัยเชียงใหม่) อาศัยดูดกินน้ำเลี้ยงเมล็ดข้าวที่รวงในระยะน้ำนม ทำให้เมล็ดลีบไม่สมบูรณ์
<em>การป้องกันกำจัด </em><br />
        เพลี้ยอ่อนที่อาศัยดูดกินน้ำเลี้ยงบริเวณราก การระบาดทำลายจะเป็นหย่อมๆ ควรใช้สารฆ่าแมลงพวกคาร์บาริล (เซฟวิน) ชนิดผงละลายน้ำ หรือชนิดน้ำ ซึ่งชนิดหลังมีประสิทธิภาพในการป้องกันกำจัดได้นานกว่าพ่นบริเวณโคนต้นข้าวและไม่ควรพ่นสารฆ่าแมลงคลุมไปทั้งแปลง ควรพ่นเฉพาะบริเวณที่มีเพลี้ยอ่อนระบาดเท่านั้น
4.<strong> ตั๊กแตน</strong> (Grasshopper, short horned grasshopper : <br />
- <em>Patanga succincta</em> Linn<br />
- <em>Locusta migratoria malilensis</em> Meyer<br />
- <em>Cyrtacanthacris tatarica</em> Linn.<br />
- <em>Chondracris rosea brunneri</em> Uvarov)<br />
วงศ์ Acrididae อันดับ Orthoptera<br />
        ที่พบเป็นประจำในแปลงเพาะปลูกข้าวไร่เป็นตั๊กแตนขนาดเล็ก (Hieroglyphus bnaian Fab.) ซึ่งมีปริมาณไม่มากนัก ทั้งตัวแก่และตัวอ่อนกัดกินใบข้าวทำให้ใบขาดแหว่ง ถ้ามีการทำลายมากจะกัดกินจนกระทั่งเหลือแต่เส้นกลางใบ ทำให้ต้นข้าวเจริญเติบโตไม่เต็มที่ ผลผลิตต่ำลงแต่ตั๊กแตนชนิดที่สำคัญ ซึ่งควรระวังการป้องกันการระบาดทำลาย ได้แก่ ตั๊กแตนปาทังกา ตั๊กแตน โลคัสตา ตั๊กแตนไซทาแคนตาคริส และตั๊กแตนคอนดาคริส ซึ่งจัดว่าเป็นแมลงศัตรูที่สำคัญ<br />
<br />
<em>การป้องกันกำจัด</em><br />
        วิธีการป้องกันกำจัดตั๊กแตนให้ได้ผล ควรใช้วิธีการทุกอย่างที่สามารถลดประชากรของตั๊กแตนให้น้อยลง ทั้งนี้ควรจะทราบชนิด อุปนิสัย นิเวศวิทยา ฯลฯ ของตั๊กแตนที่จะป้องกันกำจัดเสียก่อน จึงจะสามารถวางมาตรการในการป้องกันกำจัดได้ทั่วๆ ไป ควรทำดังนี้<br />
        1. เกษตรกรควรร่วมกันดำเนินการจับตั๊กแตนในฤดูหนาว (ธันวาคม – กุมภาพันธ์) ซึ่งถ้าอุณหภูมิต่ำกว่า 13 องศาเซลเซียสลงไป ตั๊กแตนจะเคลื่อนไหวช้าหรือแข็งตัวบินไม่ได้ซึ่งสามารถจับได้ง่ายด้วยมือเปล่า และยังนำตั๊กแตนที่จับได้ไปแปรรูปเป็นอาหารได้อีกด้วย<br />
        2. กำจัดวัชพืชที่เป็นอาหารหรือเป็นที่อาศัยของตั๊กแตนทั้งในไร่ และในบริเวณใกล้เคียง<br />
        3. ไถและพรวนดินในบริเวณที่มีตั๊กแตนอาศัยเพื่อตากดินและช่วยทำลายไข่ของตั๊กแตนที่อยู่ในดิน ซึ่งควรเริ่มดำเนินการระหว่างเดือนพฤษภาคม – มิถุนายน<br />
        4. ภายหลังการเก็บเกี่ยวควรเก็บตอซังและซากพืชให้หมด เพื่อมิให้เป็นที่อยู่อาศัยของตั๊กแตน<br />
        5. ปลูกพืชตระกูลถั่ว เช่น ถั่วลิสง สลับกับแปลงเพาะปลูกข้าวไร่ เพื่อดึงดูดตั๊กแตนให้มาอาศัยร่มเงาหลบแสงแดด ในถั่วลิสงและทำการกำจัดโดยวิธีปล่อยลูกเป็ดเข้าไปกินหรือเกษตรกรจับมาใช้ประโยชน์หรือพ่นสารฆ่าแมลงคาร์บาริลผสมน้ำและกากน้ำตาลหรือน้ำอ้อยแดง<br />
6. หมั่นตรวจแปลงเพาะปลูกอยู่เสมอ โดยเฉพาะในระยะต้นฤดูฝนตั้งแต่เดือนพฤษภาคม เป็นต้นไป ซึ่งเป็นระยะที่ตั๊กแตนเริ่มฟักเป็นตัวอ่อน หากพบตั๊กแตนในระยะนี้อยู่รวมกันเป็นจำนวนมากควรรีบทำการกำจัด<br />
7. การใช้สารฆ่าแมลงพ่นกำจัด ควรใช้ให้ถูกต้องตามชนิดและวัยของตั๊กแตน เช่นคาร์บาริล (เซฟวิน เซฟ 85 ดี เอส 85) 85 % ชนิดผงละลายน้ำซึ่งเป็นสารฆ่าแมลงชนิดกินตาย พ่นคลุมพื้นที่ที่มีตั๊กแตนซึ่งเป็นระยะตัวอ่อนอยู่หนาแน่น ในฤดูการเพาะปลูกในอัตรา 70 – 80 กรัมต่อน้ำ 20 ลิตร และผสมด้วยกากน้ำตาล (โมลาส) หรือน้ำอ้อยแดง 2 – 3 ช้อนแกง โดยพ่นบนใบข้าวหรือถั่วลิสงที่ปลูกเป็นพืชสลับ
5.<strong> เพลี้ยกระโดดหลังขาว<br />
</strong>        เพลี้ยกระโดดหลังขาว (Sogatella (= Sogata) furcifera) สามารถแยกชนิดออกจากเพลี้ยกระโดดชนิดอื่นในระยะตัวเต็มวัยได้ โดยมีแถบสีขาวตามยาวของด้านหลังส่วนอกอยู่ระหว่างฐานปีกทั้งสอง แมลงในสกุลนี้มีหลายชนิดที่พบในนาข้าว แต่ข้าวไม่ใช่พืชอาหารหลักและสามารถจำแนกแมลงแต่ละชนิดออกจากกันโดยดูที่ปีก ส่วนหัวและลักษณะอวัยวะเพศผู้ ยกเว้นเพศเมียของ S. panicicola และ S. longifurcifera <br />
<em>การเป็นศัตรูพืช</em><br />
        ถึงแม้ว่าไม่ได้เป็นแมลงพาหนะนำโรไวรัสแต่ก็พบเพลี้ยกระโดดหลังขาวได้ทั่วไป และอาจดูดกินน้ำเลี้ยงต้นข้าวทำให้แห้งตายได้ แต่สามารถป้องกันกำจัดได้โดยใช้สารฆ่าแมลง เพลี้ยกระโดดหลังขาวระบาดได้ทุกสภาพแวดล้อม<br />
<em>การเจริญเติบโต</em><br />
        ตัวเต็มวัย มีสีน้ำตาลถึงสีดำ ลำตัวสีเหลือง มีแถบสีขาวเห็นชัดอยู่ด้านหลังส่วนอกระหว่างปีกสองข้าง เพลี้ยกระโดดหลังขาว เพศเมียมีทั้งชนิดปีกสั้นและปีกยาว ส่วนเพศผู้มีแต่ชนิดปีกยาวเท่านั้น เพลี้ยกระโดดหลังขาวจะอาศัยอยู่บริเวณโคนต้นข้าว ตัวเต็มวัยเข้ามาในแปลงข้าวช่วง 30 วัน แรกหลังจากเป็นต้นกล้า ใน 1 ฤดูปลูก เพลี้ยกระโดดหลังขาวเจริญเติบโตขยายพันธุ์ได้น้อยรุ่นกว่าเพลี้ยกระโดดสีน้ำตาล และชอบดูดกินน้ำเลี้ยงจากข้าวต้นอ่อน และขยายพันธุ์เป็นพวกปีกยาว จากนั้นอพยพออกจากแปลงข้าวก่อนที่ข้าวจะออกดอก กับดักแสงไฟสามารถดักจับตัวเต็มวัยเพลี้ยกระโดดหลังขาวได้จำนวนมาก และจับได้มากที่สุดในช่วงพระจันทร์เต็มดวง<br />
ไข่ ไข่มีลักษณะและขนาดเหมือนกับไข่ของเพลี้ยกระโดดสีน้ำตาล แต่มีเปลือกหุ้มไข่ยาวกว่า เพศเมียแต่ละตัวสามารถวางไข่ได้ 300 – 500 ฟองในช่วงชีวิตประมาณ 2 สัปดาห์<br />
        ตัวอ่อน ตัวอ่อนระยะแรกของเพลี้ยกระโดดทุกชนิดจะเห็นเป็นสีขาว และไม่สามารถแยกความแตกต่างของตัวอ่อนแต่ละชนิดได้ในแปลงนา ตัวอ่อนระยะหลังๆ ของเพลี้ยกระโดดหลังขาวแยกความแตกต่างได้โดยมีจุดดำและขาวที่ส่วนท้องด้านบน<br />
การทำลาย<br />
        ตัวอ่อนและตัวเต็มวัยของเพลี้ยกระโดดหลังขาว ดูดกินน้ำเลี้ยงที่โคนกอข้าว และผลิตมูลหวานออกมาน้อยกว่าเพลี้ยกระโดดสีน้ำตาล ทำให้มีราดำเกิดขึ้นน้อยตรงรอยทำลาย ต้นข้าวที่ถูกเพลี้ยกระโดดหลังขาวจำนวนมากทำลาย มีสีเหลืองส้มใบแห้ง ต่อมาใบจะเป็นสีน้ำตาล ในระยะแรกต้นข้าวแห้งตายเป็นหย่อมๆ จุดที่ถูกทำลายจะขยายกว้างถ้าประชากรแมลงเพิ่มขึ้นอย่างต่อเนื่องโดยทั่วไปเพลี้ยกระโดดหลังขาวเข้าทำลายข้าวในระยะตั้งท้อง เพลี้ยกระโดดหลังขาวไม่เป็นแมลงพาหนะนำโรคไวรัสมาสู่ต้นข้าว<br />
<br />
<em>การจัดการ</em><br />
        วิธีเขตกรรม วิธีการเขตกรรมที่ใช้ได้ผลดีกับเพลี้ยกระโดดสีน้ำตาล สามารถใช้ได้กับเพลี้ยกระโดดหลังขาว เช่นกัน <br />
พันธุ์ต้านทาน เนื่องจากเพลี้ยกระโดดหลังขาวแพร่ระบาดออกจากแปลงนาช่วงข้าวตั้งท้อง พันธุ์ข้าวที่เก็บเกี่ยวเร็วสามารถลดจำนวนรุ่นขยายพันธุ์ของเพลี้ยกระโดดหลังขาวได้ ข้าวพันธุ์แตกกอมาก เพลี้ยกระโดดหลังขาวทั้งตัวเต็มวัยและตัวอ่อนจะมีมากกว่าข้าวพันธุ์ที่แตกกอน้อย<br />
การป้องกันกำจัดโดยชีววิธี<br />
        เพลี้ยกระโดดหลังขาวมีศัตรูธรรมชาติทำลายทุกระยะการเจริญเติบโต โดยทั่วไปประชากรของเพลี้ยกระโดดหลังขาวจึงมีน้อย การใช้สารฆ่าแมลงโดยไม่พิจารณาให้รอบคอบอาจทำลายศัตรูธรรมชาติมากกว่าเพลี้ยกระโดดหลังขาว และทำให้เกิดการระบาดของเพลี้ยกระโดดหลังขาวได้ ไข่ของเพลี้ยกระโดดหลังขาวถูกเบียนโดยแตนเบียนตัวเล็กๆ หรือถูกมวนเขียวดูดไข่ หรือไรตัวห้ำกิน ตัวอ่อนและตัวเต็มวันถูกเบียนโดยแตนเบียน dryinid หรือถูกเชื้อราทำลาย<br />
ตัวห้ำของตัวอ่อนและตัวเต็มวัยรวมทั้งตัวห้ำที่อยู่ใต้ผิวน้ำได้แก่ แมลงเหนี่ยง ด้วงดิ่ง ตัวอ่อนแมลงปอบ้าน แมลงปอเข็ม พวกที่อยู่บนผิวน้ำ ได้แก่ จิงโจ้น้ำ ก็เป็นตัวห้ำที่สำคัญด้วยเช่นกัน ตัวห้ำที่อยู่ในน้ำเหล่านี้จะคอยจับกินเพลี้ยกระโดดที่หล่นลงบนผิวน้ำ และยังสามารถจับเพลี้ยกระโดดบนใบข้าวที่อยู่ใกล้ๆ ระดับน้ำได้อีกด้วย ด้วงก้นกระดก ด้วงดิน และมวนดอกรัก รวมทั้งแมงมุมต่างๆ จับตัวอ่อนและตัวเต็มวัยเพลี้ยกระโดดหลังขาวที่อยู่ตามใบข้าวกินเป็นอาหาร<br />
แมลงปอเข็มตัวเต็มวัยจับกินเพลี้ยกระโดดที่เกาะตามใบข้าว ส่วนแมลงปอบ้านตัวเต็มวัยจับกินเพลี้ยกระโดดที่บินในนาข้าว<br />
การป้องกันกำจัดด้วยสารเคมี<br />
        วิธีการใช้สารฆ่าแมลงสำหรับเพลี้ยกระโดดหลังขาวเหมือนกับของเพลี้ยกระโดดสีน้ำตาล สารฆ่าแมลงชนิดพ่นมีประสิทธิภาพดีกว่าชนิดเม็ด อย่าใช้สารฆ่าแมลงที่เป็นสาเหตุทำให้เกิดการระบาดของแมลงเพิ่มขึ้น<br />
การสำรวจ สำรวจนาข้าวหลังจากปลูกแล้ว 30 วัน ทุกสัปดาห์จนถึงช่วงข้าวออกดอก ตบต้นข้าวเพื่อให้เพลี้ยกระโดดเคลื่อนไหวตัวและตรวจนับจำนวน สุ่มสำรวจจากข้าว 20 กอหรือจุด ตามแนวทแยงมุมของแปลงนา<br />
คำนวณค่าเฉลี่ยจำนวนแมลงต่อต้นของเพลี้ยกระโดดหลังขาวที่ตรวจนับได้ ถ้าตรวจพบเพลี้ยกระโดดดังกล่าว 1 ตัว/ต้น ให้สำรวจสัปดาห์ละ 2 ครั้ง พ่นสารฆ่าแมลงที่บริเวณโคนต้นข้าวเมื่อตรวจพบตัวอ่อนที่โตเต็มที่มากกว่า 1 ตัว/ต้น
