        1. การป้องกันกำจัดโดยวิธีกล ( mechanical control )<br />
                1.1 การใช้ตาข่ายคลุมแปลงหรือตาข่ายดักนกหรือกรงดักนก จะต้องเลือกใช้ขนาดของรูตาข่ายที่มีตาถี่พอที่จะไม่ให้นกบินลอดเข้าได้ อย่างไรก็ตามวิธีนี้จะเสียค่าใช้จ่ายสูง แต่ถ้าเก็บรักษาตาข่ายดีๆ แล้วสามารถนำมาใช้เป็นเวลาหลายๆปีเช่นตาข่ายที่ทำด้วยเชือกพลาสติก เป็นต้น สำหรับโกดังยุ้งฉางที่มีช่องหรือรูที่นกสามารถเข้าไปทำลายผลิตผลทางเกษตรหรือเข้าไปทำรังได้ ก็ควรใช้ตาข่ายชนิดตาถี่ปิดกั้นช่องนั้น ๆ เสีย อีกประการหนึ่งการก่อสร้างโกดังหรือยุ้งฉาง ส่วนที่เป็นกันสาดหรือขอบคิ้วต่าง ๆ ควรจะใช้วัสดุที่มีผิวเรียบมันและควรมีความลาดเอียงประมาณ 45 องศา และไม่ควรจะมีส่วนที่ยื่นออกไปให้นกเกาะอาศัยได้ การไล่โดยใช้หุ่นไล่กาถ้าเป็นหุ่นไล่กาชนิดที่เคลื่อนไหวได้จะได้ผลดีกว่าหุ่นนิ่ง<br />
                1.2 การไล่โดยคน ในกรณีที่แรงงานมากพอ การใช้คนไล่นกก็จะได้ผลดี<br />
                1.3 การใช้เครื่องมือทำให้เกิดเสียงดัง เพื่อให้นกตกใจบินหนี เช่น การจุดประทัดเป็นระยะๆ การใช้ปืนยิง การใช้เครื่องระเบิดโดยใช้แก๊ส (gas exploder) การใช้ปี๊บน้ำมันคว่ำแล้วผูกเชือกห้อยของหนักไว้ภายในเหมือนกระดิ่ง แล้วคอยกระตุกไล่นก เป็นต้น<br />
                1.4 การใช้วัสดุที่ทำให้แสงสะท้อนวูบวาบ เช่น ใช้กระจกเงาหรือแผ่นอลูมิเนียมเงา หรือสายเทปคาสเซทที่ไม่ใช้หรือยืดแล้ว แขวนให้ทั่วบริเวณที่ต้องการไล่ อาศัยลมพัดตามธรรมชาติทำให้วัสดุนั้น ๆ หมุนและเกิดสะท้อนแสง ทำให้นกตกใจหนีได้<br />
                1.5 การใช้เครื่องขยายเสียงหรือเทปบันทึกเสียง โดยอัดเสียงตอนที่นกตกใจกลัวหรืออัดเสียงนกเหยี่ยวหรือนกล่าเหยื่อต่าง ๆ แล้วเปิดเพื่อให้นกตกใจกลัว<br />
        2. การป้องกันกำจัดโดยวิธีเขตกรรม ( cultural practice )<br />
การจำกัดวัชพืชต่าง ๆ จะช่วยป้องกันไม่ให้นก มีแหล่งอาหารจากเมล็ดหญ้าเพิ่มขึ้นก่อนที่จะลงทำลายข้าวในนา เศษหญ้า ฟางข้าว หรือขยะอื่น ๆ ควรจะเก็บให้มิดชิดหรือทำลายเสียเพื่อไม่ให้นกนำมาใช้เป็นวัสดุทำรังพุ่มไม้ที่ไม่จำเป็นรอบๆ แปลงนาก็ควรทำลายเสียจะช่วยกำจัดแหล่งที่อยู่อาศัยของนก ตลอดจนการปลูกข้าวไร่ควรจะปลูกเพื่อให้ข้าวสุกพร้อมๆ กัน เพราะตามปกติแล้วแปลงข้าวใดที่ออกรวงก่อนมักจะถูกนกทำลายเสียหายมากกว่า <br />
        3. การป้องกันกำจัดโดยชีววิธี (biological control)<br />
เช่นการฝึกหัดสัตว์ศัตรูธรรมชาติ เช่น แมวหรือนกล่าเหยื่อต่างๆ เช่น เหยี่ยว คอย ขับไล่นกศัตรูข้าวไร่ การทำลายรังนก ไข่ หรือตัวอ่อน<br />
        4. การป้องกันกำจัดโดยใช้สารเคมี (chemical control)<br />
สารเคมีที่ทดสอบแล้วว่าใช้ได้ผลดี เป็นพวกสารไล่นก (bird repellent) ใช้พ่นให้ทั่วรวงข้าว เมื่อนกลงมากินจะเกิดอาการเข็ดและหนีไฟ สารเคมีดังกล่าวได้แก่ เมซูรอล (methiocarb 50% WP) ใช้ในอัตรา 120 กรัมต่อไร่ของสารออกฤทธิ์ หรือประมาณ 12 ช้อนแกงต่อไร่โดยผสมน้ำ 20 ลิตรหรือ 1 ปี๊บ ฉีดพ่นครั้งแรกในระยะข้าวเป็นน้ำนม และหลังจากนั้นประมาณ 12 วัน จึงฉีดพ่นอีกครั้งหนึ่งจะได้ผลดีที่สุด<br />
        5. การป้องกันกำจัดโดยวิธีผสมผสาน (integrated control)<br />
เนื่องจากนกศัตรูข้าวเป็นสัตว์ที่มีการหาอาหารและครอบครองที่อยู่ได้มาก ดังนั้นการ<br />
ป้องกันและกำจัดนก อาจจะต้องใช้หลายๆ วิธีดังกล่าวมาแล้วผสมผสานกันตามความเหมาะสมของ<br />
แต่ละพื้นที่และตามดุลยพินิจของผู้ที่จะทำการป้องกันและกำจัดแต่ละพื้นที่และตามดุลยพินิจของผู้ที่จะทำการป้องกันและกำจัด
<strong>3. กระต่ายป่าและการป้องกันกำจัด</strong>
        กระต่ายป่าเท่าที่พบในเมืองไทยมี 1 ชนิด คือ Lepus siamensis โดยปกติแล้วกระต่ายเพศเมียจะให้ลูกปีละหลายครอก แต่ละครอกจะมีลูกอ่อนเฉลี่ย 3 – 4 ตัว มักจะทำลายข้าวไร่โดยเฉพาะช่วงที่ข้าวกำลังเป็นต้นอ่อน หรืองอกได้ประมาณ 1 – 2 สัปดาห์ โดยจะออกหากินในตอนเย็นและค่ำ ลักษณะที่เห็นได้ชัดคือมูลที่หล่นอยู่ใกล้แหล่งอาหารที่ทำลาย<br />
การป้องกันกำจัดอาจจะใช้ตาข่ายหรือกรงดัก กับดัก ตามทางที่กระต่ายป่าทิ้งร่องรอยไว้หรือทิศทางที่กระต่ายป่าเข้ามาหากินในแปลงข้าวไร่ อาจจะใช้ผ้าขาวหรือพลาสติกใสผูกติดปลายไม้แล้วปักเป็นระยะๆ ประมาณ 10 – 15 เมตรต่อ 1 จุด ตามแนวรอบๆ แปลงข้าวไร่ที่ติดชายป่า หรืออาจจะใช้กระดิ่งแขวนตามแนวรอบๆ แปลง เมื่อลมพัดเกิดเสียงดังทำให้กระต่ายป่ากลัวได้<br />
