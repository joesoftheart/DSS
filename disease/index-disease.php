        ความเสียหายจากการทำลายของแมลงสิงทำให้เมล็ดข้าวเสื่อมเสียคุณภาพมากกว่าทำให้น้ำหนักเมล็ดข้าวลดลง เมล็ดข้าวที่ถูกแมลงสิงทำลาย เมื่อนำไปสีจะแตกหักได้ง่าย เกษตรกรโดยทั่วไป มักไม่ได้สูญเสียรายได้จากคุณภาพเมล็ดที่เสียหายดังกล่าว เนื่องจากโดยทั่วไปไม่มีการตรวจสอบคุณภาพเมล็ดที่มีการซื้อขายกัน <br />
